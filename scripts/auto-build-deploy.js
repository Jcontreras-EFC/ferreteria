// Funci√≥n para ejecutar comandos y mostrar salida en tiempo real
function ejecutarComando(comando, args, descripcion) {
  return new Promise((resolve) => {
    console.log(`\n[${descripcion}]`);
    console.log('‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ');
    console.log(`Ejecutando: ${comando} ${args.join(' ')}\n`);
    
    const proceso = spawn(comando, args, {
      stdio: 'inherit', // Esto muestra la salida en tiempo real
      shell: true,
      cwd: process.cwd()
    });
    
    proceso.on('close', (code) => {
      console.log('\n‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ');
      if (code === 0) {
        console.log(`‚úÖ ${descripcion} - Completado exitosamente\n`);
        resolve(true);
      } else {
        console.log(`‚ùå ${descripcion} - Fall√≥ (c√≥digo: ${code})\n`);
        resolve(false);
      }
    });
    
    proceso.on('error', (error) => {
      console.error(`‚ùå Error ejecutando ${descripcion}:`, error.message);
      resolve(false);
    });
  });
}

